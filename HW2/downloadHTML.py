output_dir = "raw_html_files"
os.makedirs(output_dir, exist_ok=True)

def download_html(uri):
    hash_name = hashlib.md5(uri.encode('utf-8')).hexdigest()

    try:
        response = requests.get(uri)
        response.raise_for_status() 
        content = response.text

        file_path = os.path.join(output_dir, f"{hash_name}.html")
        with open(file_path, "w", encoding="utf-8") as f:
            f.write(content)

        print(f"Downloaded: {uri} -> {file_path}")
        return hash_name, uri

    except requests.exceptions.RequestException as e:
        print(f"Error downloading {uri}: {e}")
        return None

